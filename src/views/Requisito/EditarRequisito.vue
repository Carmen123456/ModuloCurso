
<template>
    <validation-observer
    ref="observer"
   
  >
   <Header/>
     <v-form enctype="multipart/form-data">
    <v-container>
      <v-row>
        <v-col
          cols="12"
          sm="6"
        >
         <validation-provider
        v-slot="{ errors }"
        name="nombreRequisitos"
        rules="required|max:25|alpha_spaces|min:3"
      >
          <v-text-field
             name="nombreRequisitos" 
             id="nombreRequisitos" 
             v-model="form.nombreRequisitos"
              :counter="25"
          :error-messages="errors"
          required
            label="Nombre Tipo Requisito"
          ></v-text-field>
          </validation-provider>
           <v-btn  class="ma-2"
          color="purple"
        dark
        v-on:click="salir()" >
        Salir
        </v-btn>
         <v-btn  class="ma-2"
          color="purple"
        dark v-on:click="editar()" >
        Editar
        </v-btn>
        </v-col>
      </v-row>
    </v-container>
 </v-form>
   </validation-observer>
</template>
<script>

import axios from 'axios';
import Header from '@/components/Header.vue';
 import { required, max, min, alpha_spaces } from 'vee-validate/dist/rules'
  import { extend, ValidationObserver,ValidationProvider } from 'vee-validate'
extend('required', {
    ...required,
    message: 'El campo no puede estar vacio',
  })
extend('alpha_spaces', {
    ...alpha_spaces,
    message: 'El campo  solo debe tener caracteres alfabeticos y espacios',
  })
  extend('max', {
    ...max,
    message: 'El campo  no puede tener mÃ¡s de {length} caracteres',
  })
  extend('min', {
    ...min,
    message: 'El campo  debe tener minimo {length} caracteres',
  })

export default {
    
    name:"EditarRequisito",
    data:function() {
        return{   
            form:{
                "idRequisitos":"",
                "nombreRequisitos":"",
            },
        }
    },
    components:{
           ValidationObserver,
         ValidationProvider,
          Header
    },
    
    methods:{
            editar(){
               
                axios.put("http://localhost:5000/api/Requisito/"+this.form.idRequisitos, this.form)
                .then( data => {
                    console.log(data);
                     this.salir();
                })
            },
            salir(){
                this.$router.push("/Requisito")
            }
    },
    mounted:function(){
            this.form.idRequisitos = this.$route.params.id;
              axios.get("http://localhost:5000/api/Requisito/"+ this.form.idRequisitos)
              .then( datos => {
                  console.log(datos)
                  this.form.nombreRequisitos = datos.data.nombreRequisitos;                 
                  console.log(this.form);
              })
        

    }
}
  </script>

